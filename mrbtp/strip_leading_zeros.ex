" Strip leading zeros in source references

" Odyssey lines
%s/^"O\.0*\([\-0-9]\{-}\)\.0*\([\-0-9a-z]\{-}\)"/"O.\1.\2"/
%s/^"O\.0*\([0-9]\{-}\)\.0*\([0-9]\{-}\)-0*\([0-9a-z]\{-}\)"/"O.\1.\2-\3"/

" Iliad lines
%s/^"I\.0*\([\-0-9]\{-}\)\.0*\([\-0-9a-z]\{-}\)"/"I.\1.\2"/
%s/^"I\.0*\([0-9]\{-}\)\.0*\([0-9]\{-}\)-0*\([0-9a-z]\{-}\)"/"I.\1.\2-\3"/

" Homeric Hymn lines
%s/^"HH\.0\([0-9].*\)/"HH\1/
%s/^"\(HH\.\d\+\.\)0\+\(.*\)/"\1\2/
%s/^"\(HH[A-Za-z0-9]\+\.\)0\+\(.*\)/"\1\2/
%s/^"\(HH[A-Za-z]\+\.\d\+\.\)0\+\(.*\)/"\1\2/

%s/^"\(HH.\+\)-0\+\(.\{-}\)",\(.*\)/"\1-\2",\3/

" Never change below here
:wq
